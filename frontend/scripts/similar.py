import math


def hex_to_rgb(hex_code):
    """Convert hex color code to RGB."""
    hex_code = hex_code.lstrip("#")
    return tuple(int(hex_code[i : i + 2], 16) for i in (0, 2, 4))


def rgb_distance(color1, color2):
    """Calculate Euclidean distance between two RGB colors."""
    return math.sqrt(sum((c1 - c2) ** 2 for c1, c2 in zip(color1, color2)))


def find_similar_images(target_image_index, color_codes, all_color_codes, threshold=50):
    """Find similar images based on color codes."""
    similar_images = []
    for color_code in color_codes:
        target_color = hex_to_rgb(color_code)
        distances = []
        for i, other_color_codes in enumerate(all_color_codes):
            if i == target_image_index:
                continue
            for other_color_code in other_color_codes:
                other_color = hex_to_rgb(other_color_code)
                dist = rgb_distance(target_color, other_color)
                distances.append((i, other_color_code, dist))
        distances.sort(key=lambda x: x[2])
        similar_images.extend(distances[:5])
    similar_images.sort(key=lambda x: x[2])  # Sort by distance
    return similar_images


